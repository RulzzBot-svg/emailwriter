const fs = require('fs');
const path = require('path');

const prodraftDir = path.resolve(__dirname, '..');
const workspaceRoot = path.resolve(prodraftDir, '..');
const envPath = path.join(prodraftDir, '.env');
const extensionDir = path.join(workspaceRoot, 'extension');
const extensionConfigPath = path.join(extensionDir, 'extension-config.js');
const sharedSourcePath = path.join(workspaceRoot, 'shared', 'prodraft-shared.js');
const sharedDestPath = path.join(extensionDir, 'prodraft-shared.js');
const manifestPath = path.join(extensionDir, 'manifest.json');

const DEFAULT_API_BASE = 'http://localhost:5173';

function escapeJsString(value) {
  return String(value)
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\r?\n/g, '');
}

function parseEnvValue(envText, key) {
  const prefix = `${key}=`;
  for (const line of envText.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) {
      continue;
    }
    if (trimmed.startsWith(prefix)) {
      return trimmed.slice(prefix.length).trim();
    }
  }
  return '';
}

function normalizeOrigin(apiBase) {
  try {
    const url = new URL(apiBase);
    return `${url.protocol}//${url.host}`;
  } catch {
    return '';
  }
}

function updateManifestHostPermissions(origin) {
  if (!fs.existsSync(manifestPath)) {
    return;
  }

  const manifest = JSON.parse(fs.readFileSync(manifestPath, 'utf8'));
  const defaults = [
    'http://localhost:5173/*',
    'http://127.0.0.1:5173/*',
    'http://127.0.0.1/*',
  ];

  const next = [...defaults];
  if (origin && !next.includes(`${origin}/*`)) {
    next.push(`${origin}/*`);
  }

  manifest.host_permissions = next;
  fs.writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`, 'utf8');
}

function main() {
  let envText = '';
  if (fs.existsSync(envPath)) {
    envText = fs.readFileSync(envPath, 'utf8');
  }

  const apiBase = parseEnvValue(envText, 'PRODRAFT_API_BASE_URL') || DEFAULT_API_BASE;
  const origin = normalizeOrigin(apiBase);

  const configOutput = [
    '// Auto-generated by prodraft/scripts/sync-extension-config.cjs',
    '// Do not edit manually.',
    `globalThis.PRODRAFT_API_BASE_URL = '${escapeJsString(apiBase)}';`,
    '',
  ].join('\n');

  fs.writeFileSync(extensionConfigPath, configOutput, 'utf8');
  fs.copyFileSync(sharedSourcePath, sharedDestPath);
  updateManifestHostPermissions(origin);

  console.log('Synced extension config and shared bundle.');
  console.log(`  API base: ${apiBase}`);
}

main();
